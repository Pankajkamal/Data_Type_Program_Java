
public class NumberDataType {
	public static void main(String[]args) {
		
		//Integer Data Type  Declaration.
		
		//Byte Data Type Declaration.
		
		System.out.println("The Byte Data Type can store whole numbers from -128 to 127.");
		Byte myNum1 = 100;
		System.out.println(myNum1);
		
		//Short Data Type Declaration.
		
		System.out.println("The short data type can store whole number from -32768 to 32767");
		short myNum2 = 5000;
		System.out.println(myNum2);
		
		// integer data type declaration.
		
		System.out.println("The integer data type can store whole numbers from -2147483648 to 21477483647");
		int myNum3 = 100000;
		System.out.println(myNum3);
		
		// Long Data Type declaration.
		System.out.println("The long data type can store whole number from "
				+ "-9223372036854775808 to 92233720036854775807");
		long myNum4 = 15000000000l;
		System.out.println(myNum4);
		
		// Floating Data Type Declaration.
		
		//float data type declaration.
		System.out.println("The float data type store froctional numbers. Float data"
				+ " shold be end the value with an 'f'");
		float myNum5 = 5.75f;
		System.out.println(myNum5);
		
		// double data type declaration.
		System.out.println("Double Data type is store fractional numbers. Double data "
				+ "shold be end the value with an 'd'");
		double myNum6 = 19.9d;
		System.out.println(myNum6);
		
		}
}
